#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	main(void)
{
	char dest[] = "holaaaaa";
	char src[] = "mundo";
	int	n;

	n = 3;

	ft_strncpy(dest, src, n);
	printf("%s", dest);
}